package Start;

public class Alumno {
	
	protected String nombre;
	protected float nota;
	
public Alumno() {
	this.nombre="";
	this.nota=0f;
	
}

public String getNombre() {
	return nombre;
}

public void setNombre(String nombre) {
	this.nombre = nombre;
}

public float getNota() {
	return nota;
}

public void setNota(float nota) {
	this.nota = nota;
}
	


}
